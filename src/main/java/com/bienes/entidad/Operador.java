package com.bienes.entidad;

public class Operador {

	public int id_ope;
	public String nom_ope;
	public String dist_ope;
	public int cel_ope;
	public String estado_ope;
	
	
	public int getId_ope() {
		return id_ope;
	}
	public void setId_ope(int id_ope) {
		this.id_ope = id_ope;
	}
	public String getNom_ope() {
		return nom_ope;
	}
	public void setNom_ope(String nom_ope) {
		this.nom_ope = nom_ope;
	}
	public String getDist_ope() {
		return dist_ope;
	}
	public void setDist_ope(String dist_ope) {
		this.dist_ope = dist_ope;
	}
	public int getCel_ope() {
		return cel_ope;
	}
	public void setCel_ope(int cel_ope) {
		this.cel_ope = cel_ope;
	}
	public String getEstado_ope() {
		return estado_ope;
	}
	public void setEstado_ope(String estado_ope) {
		this.estado_ope = estado_ope;
	}
	
	
	
}
